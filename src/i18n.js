// Detect language from browser/VPN
function detectLanguage() {
  const nav = navigator.language || navigator.userLanguage || 'en';
  const code = nav.split('-')[0].toLowerCase();
  if (code === 'fr') return 'fr';
  if (code === 'es') return 'es';
  return 'en';
}

